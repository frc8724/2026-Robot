    private double distanceToHub() {
        var hubPose = translatePose(hubMidPoint);
        var robotPose = this.getState().Pose;
        var diff = hubPose.minus(robotPose);
        return Math.sqrt(diff.getX() * diff.getX() + diff.getY() * diff.getY());
    }

