    public Command tankDrive(DoubleSupplier x, DoubleSupplier y) {
        return run(() -> {
            var joystickX = -x.getAsDouble();
            var joystickY = -y.getAsDouble();
            var vx = joystickX;
            var vy = joystickY;
            var maxSpeed = 4.0;

            var angleRad = Math.atan2(vy, vx);
            var speed = Math.sqrt(vx * vx + vy * vy);

            // if we don't have a direction, lock the wheels
            if (Math.abs(joystickX) < .2 && Math.abs(joystickY) < .2) {
                this.setControl(swerveBrake);
            } else {
                this.setControl(
                        fieldCentric.withVelocityX(vx * maxSpeed)
                                .withVelocityY(vy * maxSpeed)
                                .withHeadingPID(speed * 20, 0, 0)
                                .withTargetDirection(new Rotation2d(angleRad)));
            }
        });
    }

